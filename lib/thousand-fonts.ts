// Font display names (cleaned up for UI)
export function getFontDisplayName(fontSlug: string): string {
  return fontSlug
    .replace(/-+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
    .split(' ')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}

// Generate SEO-friendly description for each font
export function getFontDescription(fontSlug: string): string {
  const displayName = getFontDisplayName(fontSlug);
  const descriptions = [
    `Transform your text with ${displayName} font style. Create unique, eye-catching text instantly.`,
    `Generate ${displayName} styled text for social media, bios, and more. Free online converter.`,
    `Convert your text to ${displayName} font. Perfect for Instagram, TikTok, Discord, and all platforms.`,
    `Create ${displayName} text effects online. Copy and paste stylish fonts anywhere.`,
    `${displayName} font generator - Transform plain text into creative, stylish fonts instantly.`,
  ];
  
  const hash = fontSlug.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0);
  return descriptions[hash % descriptions.length];
}

// Get related fonts for internal linking
export function getRelatedFonts(currentFont: string, count: number = 25): string[] {
  const allFonts = [...THOUSAND_FONTS].filter(f => f !== currentFont);
  
  // Shuffle and return random fonts
  const shuffled = allFonts.sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}
